function mostrarOperaciones(num1,num2){
    console.log("Numero 1:",num1);
    console.log("Numero 2:",num2);

    // Operaciones
    console.log("suma",Number(num1+num2));
    console.log("Resta",Number(num1-num2));
    console.log("Multiplicacion",Number(num1*num2));
    console.log("Divisio0n",Number(num1/num2));

    // ejemplo de los dos numeros
    mostrarOperaciones(16,8);

}